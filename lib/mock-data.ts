// Helper function to get city by ID
export function getCityById(id: string): City | undefined {
  return mockCities.find((city) => city.id === id);
}

// Helper function to get reviews by city ID
export function getReviewsByCityId(cityId: string): Review[] {
  return mockReviews.filter((review) => review.cityId === cityId);
}

// Helper function to get cafes by city ID
export function getCafesByCityId(cityId: string): Cafe[] {
  return mockCafes.filter((cafe) => cafe.cityId === cityId);
}

// Helper function to get top cities
export function getTopCities(limit: number = 10): City[] {
  return mockCities
    .sort((a, b) => (b.rank || 0) - (a.rank || 0))
    .slice(0, limit);
}
